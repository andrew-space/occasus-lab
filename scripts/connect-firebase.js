/* Firebase connector for Studio Occasus
   Usage: node scripts/connect-firebase.js
*/

const fs = require("fs");
const path = require("path");
const readline = require("readline");

const root = path.resolve(__dirname, "..");
const firebaseConfigPath = path.join(root, "site", "assets", "firebase-config.js");
const firebasercPath = path.join(root, ".firebaserc");

const rl = readline.createInterface({
  input: process.stdin,
  output: process.stdout,
});

function ask(question, fallback = "") {
  return new Promise((resolve) => {
    const suffix = fallback ? ` [${fallback}]` : "";
    rl.question(`${question}${suffix}: `, (answer) => {
      const value = (answer || "").trim();
      resolve(value || fallback);
    });
  });
}

function buildFirebaseConfig(values) {
  return `/* ═══════════════════════════════════════════════════
   Firebase Configuration — Occasus Lab
   Generated by scripts/connect-firebase.js
   ═══════════════════════════════════════════════════ */

var FIREBASE_CONFIG = {
  apiKey: "${values.apiKey}",
  authDomain: "${values.authDomain}",
  projectId: "${values.projectId}",
  storageBucket: "${values.storageBucket}",
  messagingSenderId: "${values.messagingSenderId}",
  appId: "${values.appId}"
};

/* Freemium limits */
var FREE_LIMITS = {
  clarity: 5,
  brand: 3,
  utm: 999,
  tone: 0,
  headline: 0,
  counter: 999,
  readability: 5,
  email: 5,
  seo: 5,
  social: 0
};

var PRO_PRICE = { monthly: 9, yearly: 79, currency: "€" };

/* Server-side billing endpoint (Firebase Functions) */
var BACKEND_CONFIG = {
  checkoutEndpoint: "https://europe-west1-${values.projectId}.cloudfunctions.net/createCheckoutSession"
};
`;
}

function buildFirebaserc(projectId) {
  return JSON.stringify(
    {
      projects: {
        default: projectId,
      },
    },
    null,
    2
  ) + "\n";
}

async function run() {
  console.log("\nStudio Occasus Firebase Connector\n");
  console.log("Collect the values from Firebase Console > Project settings > Your apps > Web app.\n");

  const projectId = await ask("Firebase projectId");
  if (!projectId) {
    console.error("projectId is required.");
    process.exitCode = 1;
    rl.close();
    return;
  }

  const apiKey = await ask("apiKey");
  const authDomain = await ask("authDomain", `${projectId}.firebaseapp.com`);
  const storageBucket = await ask("storageBucket", `${projectId}.appspot.com`);
  const messagingSenderId = await ask("messagingSenderId");
  const appId = await ask("appId");

  if (!apiKey || !messagingSenderId || !appId) {
    console.error("apiKey, messagingSenderId, and appId are required.");
    process.exitCode = 1;
    rl.close();
    return;
  }

  const values = { projectId, apiKey, authDomain, storageBucket, messagingSenderId, appId };
  fs.writeFileSync(firebaseConfigPath, buildFirebaseConfig(values), "utf8");
  fs.writeFileSync(firebasercPath, buildFirebaserc(projectId), "utf8");

  console.log("\nDone.");
  console.log(`- Updated ${path.relative(root, firebaseConfigPath)}`);
  console.log(`- Updated ${path.relative(root, firebasercPath)}`);
  console.log("\nNext commands:");
  console.log("1) firebase login");
  console.log(`2) firebase use ${projectId}`);
  console.log("3) firebase deploy --only firestore:rules");
  console.log("4) (optional) cd functions && npm install && cd .. && firebase deploy --only functions");
  console.log("\nGoogle Sign-In checklist:");
  console.log("- Firebase Authentication > Sign-in method > enable Google");
  console.log("- Authentication > Settings > Authorized domains: add localhost and your GitHub Pages domain");

  rl.close();
}

run().catch((err) => {
  console.error(err);
  process.exitCode = 1;
  rl.close();
});
